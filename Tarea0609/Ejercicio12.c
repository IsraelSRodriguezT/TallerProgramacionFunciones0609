/*Bucle For debido a que me permite demostrar que el código sea fácil de entender, y en el caso de querer 
estructurarlo para otro bucle, que sea mas facil adaptarlo*/
int bucle(int numeros, int snumeros){
    for (int i=1;i<=numeros;i++){ /*Inicializo que i sea igual a 1, establezco que el bucle termine cuando i sea menor o igual a numeros,
    y que en cada iteracion a i se le sume 1*/
        if(i%2!=0){ //Estructura Condicional Multiple para indicar hasta cuando se presente +
            if (i==(numeros-1)){
                printf("%d=", i);
            }else if(numeros%2!=0 && i==numeros){ 
                printf("%d=", i);
            }else{
                printf("%d+", i);
            }
            snumeros+=i; //Variable donde se guardan y suman los valores de i
        }
    }
    return snumeros; //Devuelvo el valor de snumeros
}
int numerosmal(int numeros){
    return (numeros<=0); //Devuelvo como valor una condicion
}
